const { PDFDocument, rgb, StandardFonts } = require('pdf-lib');
const { Document, Packer, Paragraph, TextRun, Table, TableRow, TableCell, WidthType, BorderStyle } = require('docx');
const User = require('../../models/User');
const { connectToDatabase } = require('../../config/db');

const generatePdf = async (req, res) => {
  try {
    await connectToDatabase();
    const user = await User.findById(req.user.userId);
    if (!user || !user.profileDetails || !user.profileDetails.isSubmitted) {
      return res.status(400).json({ error: 'Profile details not found or form not submitted yet' });
    }
    const details = user.profileDetails;
    const formattedDob = details.dob
      ? new Date(details.dob).toLocaleDateString('en-US', { year: 'numeric', month: 'long', day: 'numeric' })
      : 'N/A';

    const pdfDoc = await PDFDocument.create();
    const page = pdfDoc.addPage([595.28, 841.89]);
    const { width, height } = page.getSize();
    const helvetica = await pdfDoc.embedFont(StandardFonts.Helvetica);
    const helveticaBold = await pdfDoc.embedFont(StandardFonts.HelveticaBold);

    page.drawRectangle({
      x: 0,
      y: height - 120,
      width: width,
      height: 120,
      color: rgb(30 / 255, 27 / 255, 75 / 255)
    });

    page.drawText('USER PROFILE SPECIFICATION', {
      x: 50,
      y: height - 65,
      size: 20,
      font: helveticaBold,
      color: rgb(1, 1, 1)
    });

    page.drawText(`Generated on ${new Date().toLocaleDateString('en-US')}`, {
      x: 50,
      y: height - 90,
      size: 10,
      font: helvetica,
      color: rgb(165 / 255, 180 / 255, 252 / 255)
    });

    page.drawText('Personal Details', {
      x: 50,
      y: height - 170,
      size: 15,
      font: helveticaBold,
      color: rgb(30 / 255, 27 / 255, 75 / 255)
    });

    page.drawLine({
      start: { x: 50, y: height - 182 },
      end: { x: width - 50, y: height - 182 },
      thickness: 1,
      color: rgb(229 / 255, 231 / 255, 235 / 255)
    });

    let currentY = height - 215;
    const drawRow = (label, value) => {
      page.drawText(label, {
        x: 50,
        y: currentY,
        size: 11,
        font: helveticaBold,
        color: rgb(107 / 255, 114 / 255, 128 / 255)
      });
      const maxChars = 40;
      if (value.length > maxChars) {
        const line1 = value.substring(0, maxChars);
        const line2 = value.substring(maxChars);
        page.drawText(line1, {
          x: 200,
          y: currentY,
          size: 11,
          font: helvetica,
          color: rgb(17 / 255, 24 / 255, 39 / 255)
        });
        currentY -= 15;
        page.drawText(line2, {
          x: 200,
          y: currentY,
          size: 11,
          font: helvetica,
          color: rgb(17 / 255, 24 / 255, 39 / 255)
        });
      } else {
        page.drawText(value, {
          x: 200,
          y: currentY,
          size: 11,
          font: helvetica,
          color: rgb(17 / 255, 24 / 255, 39 / 255)
        });
      }
      currentY -= 15;
      page.drawLine({
        start: { x: 50, y: currentY },
        end: { x: width - 50, y: currentY },
        thickness: 0.5,
        color: rgb(243 / 255, 244 / 255, 246 / 255)
      });
      currentY -= 20;
    };

    drawRow('Full Name', details.fullName || 'N/A');
    drawRow('Date of Birth', formattedDob);
    drawRow('Email Address', details.email || 'N/A');
    drawRow('Mobile Number', details.mobile || 'N/A');
    drawRow('Residential Address', details.address || 'N/A');

    currentY -= 10;
    page.drawRectangle({
      x: 50,
      y: currentY - 50,
      width: width - 100,
      height: 60,
      color: rgb(249 / 255, 250 / 255, 251 / 255),
      borderColor: rgb(229 / 255, 231 / 255, 235 / 255),
      borderWidth: 1
    });

    page.drawText('Declaration & Integrity Statement:', {
      x: 65,
      y: currentY - 12,
      size: 9,
      font: helveticaBold,
      color: rgb(55 / 255, 65 / 255, 81 / 255)
    });

    page.drawText('The information provided above represents the accurate and official personal profile details', {
      x: 65,
      y: currentY - 26,
      size: 8.5,
      font: helvetica,
      color: rgb(107 / 255, 114 / 255, 128 / 255)
    });

    page.drawText('stored securely in the system registry.', {
      x: 65,
      y: currentY - 38,
      size: 8.5,
      font: helvetica,
      color: rgb(107 / 255, 114 / 255, 128 / 255)
    });

    page.drawText('This document was automatically generated by the web application service.', {
      x: 130,
      y: 40,
      size: 8,
      font: helvetica,
      color: rgb(156 / 255, 163 / 255, 175 / 255)
    });

    const pdfBytes = await pdfDoc.save();
    const safeName = user.username.replace(/[^a-zA-Z0-9]/g, '_');
    res.setHeader('Content-Type', 'application/pdf');
    res.setHeader('Content-Disposition', `attachment; filename="${safeName}_profile.pdf"`);
    res.setHeader('Content-Length', pdfBytes.length.toString());
    return res.send(Buffer.from(pdfBytes));
  } catch (err) {
    return res.status(500).json({ error: 'Failed to generate PDF document' });
  }
};

const generateDocx = async (req, res) => {
  try {
    await connectToDatabase();
    const user = await User.findById(req.user.userId);
    if (!user || !user.profileDetails || !user.profileDetails.isSubmitted) {
      return res.status(400).json({ error: 'Profile details not found or form not submitted yet' });
    }
    const details = user.profileDetails;
    const formattedDob = details.dob
      ? new Date(details.dob).toLocaleDateString('en-US', { year: 'numeric', month: 'long', day: 'numeric' })
      : 'N/A';

    const createCell = (text, isBold, sizeHalfPoints, color = '111827') => {
      return new TableCell({
        children: [
          new Paragraph({
            children: [
              new TextRun({
                text,
                bold: isBold,
                size: sizeHalfPoints,
                color,
                font: 'Segoe UI'
              })
            ],
            spacing: { before: 120, after: 120 }
          })
        ],
        width: { size: isBold ? 30 : 70, type: WidthType.PERCENTAGE },
        margins: { top: 120, bottom: 120, left: 150, right: 150 }
      });
    };

    const buildRow = (label, value) => {
      return new TableRow({
        children: [
          createCell(label, true, 22, '6B7280'),
          createCell(value, false, 22, '111827')
        ]
      });
    };

    const doc = new Document({
      sections: [
        {
          properties: {},
          children: [
            new Paragraph({
              children: [
                new TextRun({
                  text: 'USER PROFILE SPECIFICATION',
                  bold: true,
                  size: 32,
                  color: '1E1B4B',
                  font: 'Segoe UI'
                })
              ],
              spacing: { after: 200 }
            }),
            new Paragraph({
              children: [
                new TextRun({
                  text: `Generated on: ${new Date().toLocaleDateString('en-US')} | Secure System Registry`,
                  italics: true,
                  size: 18,
                  color: '9CA3AF',
                  font: 'Segoe UI'
                })
              ],
              spacing: { after: 400 }
            }),
            new Table({
              width: { size: 100, type: WidthType.PERCENTAGE },
              borders: {
                top: { style: BorderStyle.SINGLE, size: 6, color: 'E5E7EB' },
                bottom: { style: BorderStyle.SINGLE, size: 6, color: 'E5E7EB' },
                insideHorizontal: { style: BorderStyle.SINGLE, size: 4, color: 'F3F4F6' },
                insideVertical: { style: BorderStyle.NONE },
                left: { style: BorderStyle.NONE },
                right: { style: BorderStyle.NONE }
              },
              rows: [
                buildRow('Full Name', details.fullName || 'N/A'),
                buildRow('Date of Birth', formattedDob),
                buildRow('Email Address', details.email || 'N/A'),
                buildRow('Mobile Number', details.mobile || 'N/A'),
                buildRow('Residential Address', details.address || 'N/A')
              ]
            }),
            new Paragraph({
              spacing: { before: 600, after: 120 },
              children: [
                new TextRun({
                  text: 'Declaration & Integrity Statement',
                  bold: true,
                  size: 20,
                  color: '374151',
                  font: 'Segoe UI'
                })
              ]
            }),
            new Paragraph({
              children: [
                new TextRun({
                  text: 'The information provided above represents the accurate and official personal profile details stored securely in the system registry.',
                  size: 18,
                  color: '6B7280',
                  font: 'Segoe UI'
                })
              ]
            })
          ]
        }
      ]
    });

    const docxBuffer = await Packer.toBuffer(doc);
    const safeName = user.username.replace(/[^a-zA-Z0-9]/g, '_');
    res.setHeader('Content-Type', 'application/vnd.openxmlformats-officedocument.wordprocessingml.document');
    res.setHeader('Content-Disposition', `attachment; filename="${safeName}_profile.docx"`);
    res.setHeader('Content-Length', docxBuffer.length.toString());
    return res.send(Buffer.from(docxBuffer));
  } catch (err) {
    return res.status(500).json({ error: 'Failed to generate Word document' });
  }
};

module.exports = { generatePdf, generateDocx };
